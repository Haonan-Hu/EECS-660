import sys
import os
import time
import numpy as np
import matplotlib.pyplot as plt

def read_data(input_file):
    
    file = []
    try:
        lines = input_file.readlines()
        for line in lines:
            temp = list(map(int, line.split(',')))
            file.append(temp)
    except:
        return file
    return file

def generateResult(executeFile):

    try:
        for i in [10, 50, 100, 200]:
            start = time.time()
            execute = f'python {executeFile} ./test_files/sm_input_{str(i)}.txt > output_{str(i)}.txt'
            os.system(execute)
            end = time.time()
    except:
        print("Fail to generate")

def diff(test, label):

    Is_True = 1
    if len(test) == len(label):
        for i in range(len(test)):
            if test[i] != label[i]:
                Is_True = 0
                break;
    else:
        Is_True = 0
    return Is_True

def grading(name):
    
    score = 0
    test_files = ["output_10.txt", "output_50.txt", "output_100.txt", "output_200.txt", ]
    label_files = ["sm_output_10.txt", "sm_output_50.txt", "sm_output_100.txt", "sm_output_200.txt"]
    label_file_path = "./test_files/"

    for i in range(len(test_files)):

        #read output generated by assigment
        test_file = open(test_files[i], 'r')
        test = read_data(test_file)
        test_file.close()
        #read sample output
        label_file = open(label_file_path + label_files[i], 'r')
        label = read_data(label_file)
        label_file.close()
        #grading
        score += diff(test, label)

    print(name[:-3] + ':  ', 100 * score / len(test_files))
 
